# 07. Write a python script to calculate volume of a cuboid. Take dimensions of cuboid from user.
print("Calculate Volume of Cuboid: ");
length = float(input("Enter length of Cuboid: "));
breadth = float(input("Enter length of Cuboid: "));
height = float(input("Enter length of Cuboid: "));
print("Volume of Cuboid is: %f"%(length*breadth*height));